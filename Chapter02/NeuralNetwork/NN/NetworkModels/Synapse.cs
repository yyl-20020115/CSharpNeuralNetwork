using System;

namespace NeuralNetwork.NetworkModels;

/// <summary>   A synapse. </summary>
public class Synapse
{

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the identifier. </summary>
    ///
    /// <value> The identifier. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public Guid Id;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the input neuron. </summary>
    ///
    /// <value> The input neuron. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public Neuron InputNeuron;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the output neuron. </summary>
    ///
    /// <value> The output neuron. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public Neuron OutputNeuron;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the weight. </summary>
    ///
    /// <value> The weight. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public double Weight;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the weight delta. </summary>
    ///
    /// <value> The weight delta. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public double WeightDelta;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Initializes a new instance of the NeuralNetwork.NetworkModels.Synapse class.
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public Synapse(Guid id) 
        => Id = id;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Initializes a new instance of the NeuralNetwork.NetworkModels.Synapse class.
    /// </summary>
    ///
    /// <param name="inputNeuron">  The input neuron. </param>
    /// <param name="outputNeuron"> The output neuron. </param>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public Synapse(Neuron inputNeuron, Neuron outputNeuron)
    {
        Id = Guid.NewGuid();
        InputNeuron = inputNeuron;
        OutputNeuron = outputNeuron;
        Weight = Network.GetRandom();
    }
}
